DDS= int(input('Digite um número de 1 a 7: '))

if DDS==1:
    print ('Domingo')
elif DDS==2:
    print ('Segunda-Feira')
elif DDS==3:
    print ('Terça-Feira')
elif DDS==4:
    print ('Quarta-Feira')
elif DDS==5:
    print ('Quinta-Feira')
elif DDS==6:
    print ('Sexta-Feira')
elif DDS==7:
    print ('Sábado')
else:
    print ('Número Inválido!')